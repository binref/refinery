"""
Tests for the refinery.lib.unrar package.

Uses minimal RAR archives constructed from known byte sequences.
"""
import base64
import hashlib
import pytest

from .. import TestBase


class TestPBKDF2(TestBase):
    """
    Test RAR5 PBKDF2 key derivation against C++ test vectors from crypt5.cpp.
    """
    def test_pbkdf2_vector1(self):
        from refinery.lib.unrar.crypt import rar5_pbkdf2
        # lg2_count=0 means count = 1 << 0 = 1
        key, _, _ = rar5_pbkdf2('password', b'salt', lg2_count=0)
        expected = bytes([
            0x12, 0x0f, 0xb6, 0xcf, 0xfc, 0xf8, 0xb3, 0x2c, 0x43, 0xe7, 0x22, 0x52, 0x56, 0xc4, 0xf8, 0x37,
            0xa8, 0x65, 0x48, 0xc9, 0x2c, 0xcc, 0x35, 0x48, 0x08, 0x05, 0x98, 0x7c, 0xb7, 0x0b, 0xe1, 0x7b,
        ])
        self.assertEqual(key, expected)

    def test_pbkdf2_vector2(self):
        from refinery.lib.unrar.crypt import rar5_pbkdf2
        # lg2_count=12 means count = 1 << 12 = 4096
        key, _, _ = rar5_pbkdf2('password', b'salt', lg2_count=12)
        expected = bytes([
            0xc5, 0xe4, 0x78, 0xd5, 0x92, 0x88, 0xc8, 0x41, 0xaa, 0x53, 0x0d, 0xb6, 0x84, 0x5c, 0x4c, 0x8d,
            0x96, 0x28, 0x93, 0xa0, 0x01, 0xce, 0x4e, 0x11, 0xa4, 0x96, 0x38, 0x73, 0xaa, 0x98, 0x13, 0x4a,
        ])
        self.assertEqual(key, expected)


